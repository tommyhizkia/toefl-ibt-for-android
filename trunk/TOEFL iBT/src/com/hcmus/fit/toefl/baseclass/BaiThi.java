package com.hcmus.fit.toefl.baseclass;

public class BaiThi {
	private int id;
	private String tenBaiThi;
	private int soCauHoi;
	private String thoiGianLamBai;
	private int maDangThi;
	private int maLoaiCauHoi;
	private int maCapDo;
	private int maChuDe;
	private int daLam;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTenBaiThi() {
		return tenBaiThi;
	}

	public void setTenBaiThi(String tenBaiThi) {
		this.tenBaiThi = tenBaiThi;
	}

	public int getSoCauHoi() {
		return soCauHoi;
	}

	public void setSoCauHoi(int soCauHoi) {
		this.soCauHoi = soCauHoi;
	}

	public String getThoiGianLamBai() {
		return thoiGianLamBai;
	}

	public void setThoiGianLamBai(String thoiGianLamBai) {
		this.thoiGianLamBai = thoiGianLamBai;
	}

	public int getMaDangThi() {
		return maDangThi;
	}

	public void setMaDangThi(int maDangThi) {
		this.maDangThi = maDangThi;
	}

	public int getMaLoaiCauHoi() {
		return maLoaiCauHoi;
	}

	public void setMaLoaiCauHoi(int maLoaiCauHoi) {
		this.maLoaiCauHoi = maLoaiCauHoi;
	}

	public int getMaCapDo() {
		return maCapDo;
	}

	public void setMaCapDo(int maCapDo) {
		this.maCapDo = maCapDo;
	}

	public int getMaChuDe() {
		return maChuDe;
	}

	public void setMaChuDe(int maChuDe) {
		this.maChuDe = maChuDe;
	}

	public int getDaLam() {
		return daLam;
	}

	public void setDaLam(int daLam) {
		this.daLam = daLam;
	}
}
